#ifndef LOG_PRINTF
#if !defined(NDEBUG)
#include <stdio.h>
#define LOG_PRINTF(FMT, ...)    \
  do {                          \
    printf(FMT, ##__VA_ARGS__); \
  } while(0)
#else
#define LOG_PRINTF(FMT, ...) (void)0;
#endif  //! defined(NDEBUG)
#endif  // LOG_PRINTF

#define LOG_F(FMT, ...) LOG_PRINTF("[FATAL] %s(): " FMT "\n", __FUNCTION__, ##__VA_ARGS__)
#define LOG_E(FMT, ...) LOG_PRINTF("[ERROR] %s(): " FMT "\n", __FUNCTION__, ##__VA_ARGS__)
#define LOG_W(FMT, ...) LOG_PRINTF("[WARN ] %s(): " FMT "\n", __FUNCTION__, ##__VA_ARGS__)
#define LOG_I(FMT, ...) LOG_PRINTF("[INFO ] %s(): " FMT "\n", __FUNCTION__, ##__VA_ARGS__)
#define LOG_D(FMT, ...) LOG_PRINTF("[DEBUG] %s(): " FMT "\n", __FUNCTION__, ##__VA_ARGS__)

